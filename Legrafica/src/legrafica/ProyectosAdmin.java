    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String categoria = "pagina_web";

        if (jRadioButton1.isSelected() == true) {
            categoria = "pagina_web";

        }
        if (jRadioButton2.isSelected() == true) {
            categoria = "tienda_en_linea";

        }
        if (jRadioButton3.isSelected() == true) {
            categoria = "app_movil";

        }
        if (jRadioButton7.isSelected() == true) {
            categoria = "app_movil_tienda";

        }
        if (jRadioButton8.isSelected() == true) {
            categoria = "redes_sociales";

        }
        if (jRadioButton9.isSelected() == true) {
            categoria = "google_ads";

        }
        if (jRadioButton10.isSelected() == true) {
            categoria = "branding";

        }
        if (jRadioButton11.isSelected() == true) {
            categoria = "diseno";

        }
        if (jTextField2.getText().equals("")) {
            JDialog frame = new JDialog();
            JOptionPane.showMessageDialog(frame, "Favor de llenar todos los campos para continuar.");
        } else {
            try {
                Connection con = ConnectDB.getConnection();
                String sql = "INSERT INTO `proyecto` (`id`, `nombre`, `categoria`, `status`, `cronograma`) "
                        + "VALUES ('', ?, '" + categoria + "', 'por_hacer', NULL);";
                PreparedStatement ps = con.prepareStatement(sql);
                ps.setString(1, jTextField2.getText());

                ps.executeUpdate();
                limpiarCampos();
                llenarTabla();
            } catch (SQLException ex) {
                Logger.getLogger(ClientesAdmin.class.getName()).log(Level.SEVERE, null, ex);
            }

        }


    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int fila = jTable1.getSelectedRow();
        jTextField1.setText(Integer.toString((Integer) jTable1.getValueAt(fila, 0)));
        jTextField2.setText((String) jTable1.getValueAt(fila, 1));

        String categoria = ((String) jTable1.getValueAt(fila, 2));
        String estado = ((String) jTable1.getValueAt(fila, 3));
        int cronograma=(Integer)jTable1.getValueAt(fila, 4);
        

        if (categoria.equals("pagina_web")) {
            jRadioButton1.setSelected(true);
        }
        if (categoria.equals("tienda_en_linea")) {
            jRadioButton2.setSelected(true);
        }
        if (categoria.equals("app_movil")) {
            jRadioButton3.setSelected(true);
        }
        if (categoria.equals("app_movil_tienda")) {
            jRadioButton7.setSelected(true);
        }
        if (categoria.equals("redes_sociales")) {
            jRadioButton8.setSelected(true);
        }
        if (categoria.equals("google_ads")) {
            jRadioButton9.setSelected(true);
        }
        if (categoria.equals("branding")) {
            jRadioButton10.setSelected(true);
        }
        if (categoria.equals("diseno")) {
            jRadioButton11.setSelected(true);
        }

        if (estado.equals("terminado")) {
            jRadioButton4.setSelected(true);
            jButton4.setEnabled(false);
            jButton5.setEnabled(true);
        }
        if (estado.equals("en_proceso")) {
            jRadioButton5.setSelected(true);
            jButton4.setEnabled(false);
            jButton5.setEnabled(true);
        }
        if (estado.equals("por_hacer")) {
            jRadioButton6.setSelected(true);
            jButton4.setEnabled(true);
            jButton5.setEnabled(false);

        }
        
        if(cronograma==0){
            jButton4.setEnabled(true);
            jButton5.setEnabled(false);
        }
        else{
            jButton4.setEnabled(false);
            jButton5.setEnabled(true);
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

