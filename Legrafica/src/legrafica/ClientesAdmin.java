    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int fila = jTable1.getSelectedRow();
        jTextField1.setText(Integer.toString((Integer) jTable1.getValueAt(fila, 0)));
        jTextField2.setText((String) jTable1.getValueAt(fila, 1));
        jTextField3.setText((String) jTable1.getValueAt(fila, 2));
        jTextField4.setText((String) jTable1.getValueAt(fila, 3));
        jTextField5.setText((String) jTable1.getValueAt(fila, 4));
        jTextField6.setText(Integer.toString((Integer)jTable1.getValueAt(fila, 6)));
        String estado = ((String) jTable1.getValueAt(fila, 5));
        if (estado.equals("potencial")) {
            jRadioButton1.setSelected(true);
            jButton5.setEnabled(false);
        }
        if (estado.equals("enProyecto")) {
            jRadioButton2.setSelected(true);
            jButton4.setEnabled(false);
            jButton5.setEnabled(true);
        }
        if (estado.equals("deshabilitado")) {
            jRadioButton3.setSelected(true);
            jButton4.setEnabled(false);
            jButton5.setEnabled(false);

        }

    }//GEN-LAST:event_jButton3ActionPerformed

